// generate-icons-index.js
//
// This script scans asset directories for SVG files and automatically generates
// `index.ts` files exporting each one using PascalCase names. This allows imports like:
//
//   import { Copy, TerminalWindow } from "~/assets/img/icons";
//   import { Logo, Favicon } from "~/assets/img/logos";
//
// Run manually with `pnpm generate-icons` or integrate into your build step.

import fs from "node:fs";
import path from "node:path";
import { pathToFileURL } from "node:url";

// Asset directories to process
const ASSET_DIRS = {
    icons: path.resolve("src/assets/img/icons"),
    logos: path.resolve("src/assets/img/logos"),
};

// Header to prepend to generated files.
const PREAMBLE = `// This file is auto-generated by the generate-icons-index.js script.
// Do not edit this file directly. Run 'pnpm generate-icons' to update it.
`;
const TIMESTAMP_PREFIX = "// Generated on ";

function buildStableContent(lines) {
    return PREAMBLE + lines.join("\n") + "\n";
}

function buildOutputContent(lines) {
    return PREAMBLE + `${TIMESTAMP_PREFIX}${new Date().toISOString()}\n` + lines.join("\n") + "\n";
}

function normalizeGeneratedContent(content) {
    return content.replace(/^\/\/ Generated on .*\r?\n/m, "");
}

function hasGeneratedTimestamp(content) {
    return content.includes(TIMESTAMP_PREFIX);
}

/**
 * Converts a filename like `copy-icon.svg` or `terminal_window.svg` to PascalCase: CopyIcon,
 * TerminalWindow
 *
 * @param {string} filename - The SVG filename (e.g., "copy-icon.svg")
 * @returns {string} - PascalCase version without extension
 */
function toPascalCase(filename) {
    return filename
        .replace(/\.svg$/, "") // Remove .svg extension
        .replace(/(^\w|[-_]\w)/g, (match) => match.replace(/[-_]/, "").toUpperCase());
}

/**
 * Generates an `index.ts` file exporting all SVGs in a specified asset directory using named
 * exports based on PascalCase identifiers.
 *
 * @param {{ assetType?: "icons" | "logos"; quiet?: boolean }} [options]
 * @returns {{ changed: boolean, count: number }}
 */
export function generateIconsIndex(options = {}) {
    const { assetType = "icons", quiet = false } = options;

    const assetDir = ASSET_DIRS[assetType];
    if (!assetDir) {
        throw new Error(`Unknown asset type: ${assetType}`);
    }

    const outputFile = path.join(assetDir, "index.ts");

    // Get all .svg files in the directory
    const files = fs.readdirSync(assetDir).filter((f) => f.endsWith(".svg"));

    // Create a list of export lines like:
    // export { default as IconName } from "./icon-name.svg";
    const lines = files
        .sort()
        .map((f) => `export { default as ${toPascalCase(f)} } from "./${f}";`);

    const nextStableContent = buildStableContent(lines);
    const nextContent = buildOutputContent(lines);
    const currentContent = fs.existsSync(outputFile)
        ? fs.readFileSync(outputFile, "utf8")
        : null;

    if (
        currentContent !== null
        && hasGeneratedTimestamp(currentContent)
        && normalizeGeneratedContent(currentContent) === nextStableContent
    ) {
        if (!quiet) {
            console.log(
                `= ${assetType.charAt(0).toUpperCase() + assetType.slice(1)} exports already up to date (${files.length} files).`,
            );
        }

        return { changed: false, count: files.length };
    }

    // Write to the output file only when exports changed
    fs.writeFileSync(outputFile, nextContent);

    if (!quiet) {
        console.log(
            `✓ Generated ${outputFile} with ${files.length} ${assetType}.`,
        );
    }

    return { changed: true, count: files.length };
}

const invokedDirectly = import.meta.url === pathToFileURL(process.argv[1] ?? "").href;

if (invokedDirectly) {
    generateIconsIndex({ assetType: "icons" });
    generateIconsIndex({ assetType: "logos" });
}
